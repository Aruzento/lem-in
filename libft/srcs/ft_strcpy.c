#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int i;

	i = -1;
	while (src[++i])
		((char *)dest)[i] = ((char *)src)[i];
	((char *)dest)[i] = '\0';
	return (dest);
}
