#include "string.h"

int		ft_isalnum(int c)
{
	if ((((c - 48) >= 0) && ((c - 48) < 10))
		|| (((c <= 122) && (c >= 97)) || ((c <= 90) && (c >= 65))))
		return (1);
	return (0);
}
